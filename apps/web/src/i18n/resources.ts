export function flattenTranslationKeys(value: object, prefix = ''): string[] {
  return Object.entries(value).flatMap(([key, child]) => {
    const path = prefix.length > 0 ? `${prefix}.${key}` : key;
    return typeof child === 'string' ? [path] : flattenTranslationKeys(child as object, path);
  });
}
